"""
    闭包
        作用：
            保存外函数栈帧,使外函数调用后栈帧不释放
        三大要素
            有外有内
            内访问外
            外返回内
"""


def func01():
    a = 10 # 午饭10道菜

    def func02(): #晚饭吃剩菜
        print(a)

    # return func02() # 返回内函数的返回值
    return func02  # 返回内函数

# 调用外函数,接收内函数
res = func01()
# ...
# 反复调用内函数
res()
# ...
res()
# ...
res()
